drop table if exists PARTY;

drop table if exists PROVINCE;

drop table if exists SUFFRAGE;

/*==============================================================*/
/* Table: PARTY                                                 */
/*==============================================================*/
create table PARTY
(
   PAR_ID               int AUTO_INCREMENT not null,
   PAR_NAME             longtext not null,
   primary key (PAR_ID)
);

/*==============================================================*/
/* Table: PROVINCE                                              */
/*==============================================================*/
create table PROVINCE
(
   PRO_ID               int AUTO_INCREMENT not null,
   PRO_NAME             longtext not null,
   primary key (PRO_ID)
);

/*==============================================================*/
/* Table: SUFFRAGE                                              */
/*==============================================================*/
create table SUFFRAGE
(
   PRO_ID               int not null,
   PAR_ID               int not null,
   SUF_VOTE             int,
   primary key (PRO_ID, PAR_ID)
);

alter table SUFFRAGE add constraint FK_SUFFRAGE foreign key (PAR_ID)
      references PARTY (PAR_ID) on delete restrict on update restrict;

alter table SUFFRAGE add constraint FK_SUFFRAGE2 foreign key (PRO_ID)
      references PROVINCE (PRO_ID) on delete restrict on update restrict;

